// Fungsi pembuatan pohon keluarga tetap sama seperti sebelumnya...
document.addEventListener("DOMContentLoaded", () => {
    const container = document.getElementById("family-tree-container");

    if (!container) {
        console.error("Container tidak ditemukan!");
        return;
    }

    // Fungsi membuat pohon keluarga secara dinamis
    function createTree(data, container, generation = 1) {
        const node = document.createElement("div");
        node.className = "node";
        node.textContent = `${data.name}${data.spouse ? " & " + data.spouse : ""}`;
        node.dataset.generation = generation;

        const childrenContainer = document.createElement("div");
        childrenContainer.className = "children";

        // Tambahkan event click untuk toggle visibilitas
        node.addEventListener("click", (e) => {
            e.stopPropagation();
            childrenContainer.classList.toggle("active");
        });

        // Tambahkan node ke container utama
        container.appendChild(node);

        // Buat elemen anak jika ada
        if (data.children && data.children.length > 0) {
            data.children.forEach((child) => createTree(child, childrenContainer, generation + 1));
            container.appendChild(childrenContainer);
        }
    }

    // Mulai membuat pohon keluarga
    createTree(familyData, container);

    // Fungsi pencarian
    document.getElementById("searchButton").addEventListener("click", () => {
        const searchName = document.getElementById("searchInput").value.trim().toLowerCase();
        if (!searchName) return alert("Masukkan nama untuk mencari!");

        const result = findPerson(familyData, searchName);
        if (result) {
            alert(`Nama ditemukan: ${result.name}\nKeturunan: ${result.children?.map((child) => child.name).join(", ") || "Tidak ada"}`);
        } else {
            alert("Nama tidak ditemukan!");
        }
    });

    function findPerson(data, name) {
        if (data.name.toLowerCase() === name) return data;
        if (!data.children) return null;

        for (const child of data.children) {
            const result = findPerson(child, name);
            if (result) return result;
        }
        return null;
    }
});
